frase = "curso em video python"
print(frase.replace('python', 'android')) # substituir onde estiver 'python' por 'android'

print(frase.upper()) # frase em letra maiuscula

print(frase.lower()) # frase em letra minuscula

print(frase.capitalize()) # primeira letra da frase em maiuscula

print(frase.title()) # todas as palavras com a primeira letra em maiuscula

print(frase.strip()) # remove espaços no começo e no fim

